// Helper pour créer un SpatieMedia valide
const createMedia = (
  id: number,
  productId: string,
  fileName: string,
  originalUrl: string,
  orderColumn: number = 1,
): SpatieMedia => ({
  id,
  model_type: "Product",
  model_id: productId,
  uuid: `uuid-${id}`,
  collection_name: "products",
  name: fileName.replace(".jpg", ""),
  file_name: fileName,
  mime_type: "image/jpeg",
  disk: "public",
  conversions_disk: "public",
  size: 1024000,
  manipulations: {},
  custom_properties: {},
  generated_conversions: {},
  responsive_images: {},
  order_column: orderColumn,
  created_at: "2025-01-01T00:00:00.000Z",
  updated_at: "2025-01-01T00:00:00.000Z",
  original_url: originalUrl,
  preview_url: originalUrl.replace("q=80&w=987", "q=80&w=400"),
});

